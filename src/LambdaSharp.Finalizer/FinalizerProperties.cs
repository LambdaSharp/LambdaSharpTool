namespace LambdaSharp.Finalizer {

    /// <summary>
    /// The <see cref="FinalizerProperties"/> class describes the LambdaSharp <i>Finalizer</i> resource properties.
    /// These properties are automatically set by the LambdaSharp compiler.
    /// </summary>
    public class FinalizerProperties {

        //--- Properties ---

        /// <summary>
        /// The <see cref="DeploymentChecksum"/> holds the deployment checksum generated by the LambdaSharp compiler.
        /// </summary>
        /// <value>The compiler generated checksum of the current deployment.</value>
        public string? DeploymentChecksum { get; set; }

        /// <summary>
        /// The <see cref="ModuleVersion"/> holds the LambdaSharp module version.
        /// </summary>
        /// <value>The module version.</value>
        public string? ModuleVersion { get; set; }
    }
}